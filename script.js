/** отримати всі рецепти з localStorage */
function getRecipes() {
  try {
    const data = localStorage.getItem(STORAGE_KEY);
    return data ? JSON.parse(data) : [];
  } catch (e) {
    console.error('Помилка читання рецептів:', e);
    return [];
  }
}

/** зберігаємо масив рецептів в localStorage */
function saveRecipes(recipes) {
  try {
    localStorage.setItem(STORAGE_KEY, JSON.stringify(recipes));
    return true;
  } catch (e) {
    console.error('Помилка збереження:', e);
    return false;
  }
}

/** отримуємо рецепт за ID */
function getRecipeById(id) {
  return getRecipes().find(r => r.id === id) || null;
}

/** додаємо новий рецепт */
function addRecipe(recipeData) {
  const recipes = getRecipes();
  const newRecipe = {
    ...recipeData,
    id: 'recipe_' + Date.now(),
    createdAt: Date.now()
  };
  recipes.unshift(newRecipe);
  saveRecipes(recipes);
  return newRecipe;
}

/** оновлюємо існуючий рецепт */
function updateRecipe(id, recipeData) {
  const recipes = getRecipes();
  const idx = recipes.findIndex(r => r.id === id);
  if (idx === -1) return false;
  recipes[idx] = { ...recipes[idx], ...recipeData, id, updatedAt: Date.now() };
  saveRecipes(recipes);
  return true;
}

/** видаляємо рецепт за ID */
function deleteRecipe(id) {
  const recipes = getRecipes().filter(r => r.id !== id);
  saveRecipes(recipes);
}
function initSampleData() {
  const VERSION_KEY = 'recipe_book_version';
  const CURRENT_VERSION = '1.1'; 
  const storedVersion = localStorage.getItem(VERSION_KEY);

  if (getRecipes().length === 0 || storedVersion !== CURRENT_VERSION) {
    // зберігаємо наявні користувацькі рецепти (не зі стандартних)
    const existing = getRecipes().filter(r => !r.id.match(/^recipe_\d+$/));
    saveRecipes([...SAMPLE_RECIPES, ...existing]);
    localStorage.setItem(VERSION_KEY, CURRENT_VERSION);
  }
}

